#import "UIColor+CBColorScheme.h"

@implementation UIColor (CBColorScheme)

// Color Scheme can be found at: http://pltts.me/palettes/1249867

+ (UIColor *)CBBrownColor
{
    return [UIColor colorWithRed:0.173 green:0.090 blue:0.098 alpha:1];
}

+ (UIColor *)CBDarkGreenColor
{
    return [UIColor colorWithRed:0.000 green:0.278 blue:0.243 alpha:1];
}

+ (UIColor *)CBLightGreenColor
{
    return [UIColor colorWithRed:0.000 green:0.478 blue:0.271 alpha:1];
}

+ (UIColor *)CBLimeGreenColor
{
    return [UIColor colorWithRed:0.612 green:0.706 blue:0.000 alpha:1];
}

+ (UIColor *)CBGoldenColor
{
    return [UIColor colorWithRed:0.992 green:0.741 blue:0.000 alpha:1];
}

@end
